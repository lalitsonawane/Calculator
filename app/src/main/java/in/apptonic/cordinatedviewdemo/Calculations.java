public class Calculations {

    public Calculations() {}

    public int addition(int first, int second){

        int result = first + second;
        return result;
    }

    public int subtraction(int first, int second){

        int result = first - second;
        return result;
    }

    public int devide(int first, int second){

        int result = first / second;
        return result;
    }
}
